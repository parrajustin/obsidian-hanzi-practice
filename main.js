var CedictParser = class {
  constructor() {
    this.simplifiedTrie = new Trie();
    this.traditionalTrie = new Trie();
  }
  async loadDictionary(app, dictPath) {
    try {
      const fileResult = await FileUtil.fetchFile(app, dictPath, "RAW" /* RAW */);
      if (!fileResult.ok) {
        return Err(new StatusError(13 /* INTERNAL */, `Failed to load dict: ${fileResult.val.message}`));
      }
      const bytes = fileResult.val;
      let text;
      if (bytes.length >= 2 && bytes[0] === 31 && bytes[1] === 139) {
        text = zlib.gunzipSync(Buffer.from(bytes)).toString("utf-8");
      } else {
        text = new TextDecoder("utf-8").decode(bytes);
      }
      this.parse(text);
      return Ok(true);
    } catch (e) {
      return Err(new StatusError(13 /* INTERNAL */, `Error loading dict: ${e.message}`));
    }
  }
  parse(text) {
    const lines = text.split("\n");
    const regex = /^(\S+)\s(\S+)\s\[([^\]]+)\]\s\/(.+)\//;
    for (const line of lines) {
      if (line.startsWith("#") || !line.trim())
        continue;
      const match = line.match(regex);
      if (match) {
        const traditional = match[1];
        const simplified = match[2];
        const pinyin = match[3];
        const english = match[4];
        const jsonStr = JSON.stringify({ traditional, simplified, pinyin, english });
        this.simplifiedTrie.insert(simplified, jsonStr);
        this.traditionalTrie.insert(traditional, jsonStr);
      }
    }
  }
  /**
   * MaxMatch Tokenizer
   */
  tokenize(text) {
    const tokens = [];
    let i = 0;
    while (i < text.length) {
      let longestMatch = "";
      let current = this.simplifiedTrie.root;
      let j = i;
      let lastMatchIndex = -1;
      while (j < text.length && current.children.has(text[j])) {
        current = current.children.get(text[j]);
        if (current.isEndOfWord) {
          lastMatchIndex = j;
        }
        j++;
      }
      if (lastMatchIndex !== -1) {
        const match = text.substring(i, lastMatchIndex + 1);
        tokens.push(match);
        i = lastMatchIndex + 1;
      } else {
        tokens.push(text[i]);
        i++;
      }
    }
    return tokens;
  }
};

